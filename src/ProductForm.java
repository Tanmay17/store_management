    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        try {
            String s = jComboBox1.getSelectedItem().toString();
            if (!s.equals("Select Product Id")) {
                int index = s.indexOf("(");
                int id = Integer.parseInt(s.substring(0, index));
                Statement st = getConnection();
                ResultSet rs = st.executeQuery(
                 "select * from details where product_id="+id);
                if (rs.next()) {
                    jTextField6.setText(rs.getString(2));
                    jTextField7.setText(rs.getFloat(3) + "");
                    jTextField8.setText(rs.getString(4));
                }
                rs.close();
                closeConnection();
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_jComboBox1ItemStateChanged

