    public void updateComboBox() {
        try {
            jComboBox1.removeAllItems();
            jComboBox2.removeAllItems();
            jComboBox1.addItem("Select Product Id");
            Statement st = getConnection();
            ResultSet rs = st.executeQuery(
             "select product_id,priduct_name from details");
            while (rs.next()) {
                jComboBox1.addItem(rs.getInt(1)
                        + "(" + rs.getString(2) + ")");
                jComboBox2.addItem(rs.getInt(1)+"");
            }
            closeConnection();
        } catch (Exception e) {
        }
    }

    public void updateTable() {
        try {
            for (int row = model.getRowCount() - 1; row >= 0;
                    row--) {
                model.removeRow(row);
            }
            Statement st = getConnection();
            ResultSet rs = st.executeQuery(
                    "select * from details");
            while (rs.next()) {
                String s[] = new String[4];
                s[0] = rs.getInt(1) + "";
                s[1] = rs.getString(2);
                s[2] = rs.getFloat(3) + "";
                s[3] = rs.getString(4);
                model.addRow(s);
            }
            closeConnection();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

