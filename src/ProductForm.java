    public int getLatestId() {
        try {
            Statement st = getConnection();
            ResultSet rs = st.executeQuery("select max(product_id) from details");
            if (rs.next()) {
                id = rs.getInt(1);
            }
            id = id + 1;
            closeConnection();
        } catch (Exception e) {
            System.out.println(e);
        }
        return id;
    }
